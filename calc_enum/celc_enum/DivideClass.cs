
namespace celc_enum
{
    public static class DivideClass
    {
        public static int Divide(int n1, int n2)
        {
            return n1 / n2;
        }
        public static float Divide(float n1, float n2)
        {
            return n1 / n2;
        }
        public static long Divide(long n1, long n2)
        {
            return n1 / n2;
        }

        public static double Divide(double n1, double n2)
        {
            return n1 / n2;
        }
    }
}
