
namespace celc_enum
{
        public static class SubtractClass
        {
            public static int Subtract(int n1, int n2)
            {
                return n1 - n2;
            }
            public static float Subtract(float n1, float n2)
            {
                return n1 - n2;
            }
            public static long Subtract(long n1, long n2)
            {
                return n1 - n2;
            }

            public static double Subtract(double n1, double n2)
            {
                return n1 - n2;
            }
        }
}
